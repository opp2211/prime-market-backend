    on platform_account_transactions (ref_type, ref_code, created_at desc)
    where ref_type is not null and ref_code is not null;

create or replace function fn_platform_account_transactions_apply_balance()
    returns trigger
    language plpgsql
as $$
begin
    update platform_accounts
    set balance = balance + new.amount
    where id = new.platform_account_id;

    return new;
end;
$$;

create trigger trg_platform_account_transactions_apply_balance
    after insert on platform_account_transactions
    for each row
execute function fn_platform_account_transactions_apply_balance();

create or replace function fn_platform_account_transactions_block_mutation()
    returns trigger
    language plpgsql
as $$
begin
    if tg_op = 'UPDATE' then
        if new.id <> old.id
            or new.group_key is distinct from old.group_key
            or new.platform_account_id <> old.platform_account_id
            or new.amount <> old.amount then
            raise exception 'Updating immutable platform transaction fields is not allowed';
        end if;

        return new;
    end if;

    if tg_op = 'DELETE' then
        raise exception 'Deleting rows from platform_account_transactions is not allowed';
    end if;

    return null;
end;
$$;

create trigger trg_platform_account_transactions_block_update
    before update on platform_account_transactions
    for each row
execute function fn_platform_account_transactions_block_mutation();

create trigger trg_platform_account_transactions_block_delete
    before delete on platform_account_transactions
    for each row
execute function fn_platform_account_transactions_block_mutation();

create table user_currency_conversions
(
    id                  bigint primary key generated by default as identity,
    public_code         varchar(16)    not null default generate_public_code('CNV'),
    user_id             bigint         not null references users (id) on delete cascade,
    from_account_id     bigint         not null references user_accounts (id),
    to_account_id       bigint         not null references user_accounts (id),
    from_currency_code  varchar(16)    not null references currencies (code),
    to_currency_code    varchar(16)    not null references currencies (code),
    from_amount         decimal(13, 4) not null,
    to_amount           decimal(13, 4) not null,
    rate                decimal(20, 8) not null,
    rate_source         varchar(32)    not null,
    status              varchar(32)    not null default 'COMPLETED',
    created_at          timestamptz    not null default now(),

    constraint uq_user_currency_conversions_public_code unique (public_code),
    constraint ck_user_currency_conversions_amounts_positive
        check (from_amount > 0 and to_amount > 0),
    constraint ck_user_currency_conversions_rate_positive check (rate > 0),
    constraint ck_user_currency_conversions_different_currencies
        check (from_currency_code <> to_currency_code),
    constraint ck_user_currency_conversions_status check (status in ('COMPLETED'))
);

create index idx_user_currency_conversions_user_created_at
    on user_currency_conversions (user_id, created_at desc, id desc);

