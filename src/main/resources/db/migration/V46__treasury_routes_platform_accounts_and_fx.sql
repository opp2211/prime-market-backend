create table deposit_payment_routes
(
    id                  bigint primary key generated by default as identity,
    deposit_method_id   bigint         not null references deposit_methods (id) on delete cascade,
    treasury_account_id bigint         not null references treasury_accounts (id) on delete restrict,
    title               text           not null,
    payment_details     jsonb          not null default '{}'::jsonb,
    min_amount          decimal(13, 4),
    max_amount          decimal(13, 4),
    priority            int            not null default 100,
    is_active           boolean        not null default true,
    note                text,
    created_at          timestamptz    not null default now(),
    updated_at          timestamptz    not null default now(),

    constraint ck_deposit_payment_routes_min_positive check (min_amount is null or min_amount > 0),
    constraint ck_deposit_payment_routes_max_positive check (max_amount is null or max_amount > 0),
    constraint ck_deposit_payment_routes_min_lte_max
        check (min_amount is null or max_amount is null or min_amount <= max_amount)
);

create index idx_deposit_payment_routes_method_active_priority
    on deposit_payment_routes (deposit_method_id, is_active, priority, id);

create index idx_deposit_payment_routes_treasury_account
    on deposit_payment_routes (treasury_account_id);

create trigger trg_deposit_payment_routes_touch_updated_at
    before update on deposit_payment_routes
    for each row
execute function touch_updated_at();

